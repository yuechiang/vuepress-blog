– GRUB2 loads the vmlinuz kernel image file into memory and extracts the contents of the initramfs image file into a temporary, memory-based file system (tmpfs).
– The initial RAM disk (initrd) is an initial root file system that is mounted before the real root file system.</p>
<h3 id="initramfs" tabindex="-1"><a class="header-anchor" href="#initramfs" aria-hidden="true">#</a> initramfs</h3>
<p>– The job of the initial RAM file system is to preload the block device modules, such as for IDE, SCSI, or RAID, so that the root file system, on which those modules normally reside, can then be accessed and mounted.
– The initramfs is bound to the kernel and the kernel mounts this initramfs as part of a two-stage boot process.
– The dracut utility creates initramfs whenever a new kernel is installed.
– Use the lsinitrd command to view the contents of the image created by dracut:</p>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code><span class="token comment"># lsinitrd | less</span>
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><h2 id="_4-loading-the-kernel" tabindex="-1"><a class="header-anchor" href="#_4-loading-the-kernel" aria-hidden="true">#</a> 4. Loading the kernel</h2>
<p>– The kernel starts the systemd process with a process ID of 1 (PID 1).</p>
<p><img src="/img/systemd-first-process-started-with-PID-1-RHEL-7.png" alt="ps"></p>
<p>– It also loads the necessary driver modules from initrd image.
– The boot loader (GRUB2) may present a boot menu to the user, or can be configured to automatically start a default operating system.
– To load Linux, the kernel is loaded together with the initramfs. The initramfs contains kernel modules for all hardware that is required to boot, as well as the initial scripts required to proceed to the next stage of booting.
– On RHEL 7, the initramfs contains a complete operational system (which may be used for troubleshooting purposes).</p>
<h2 id="_5-starting-systemd" tabindex="-1"><a class="header-anchor" href="#_5-starting-systemd" aria-hidden="true">#</a> 5. Starting systemd</h2>
<p>– The kernel starts the systemd process with a process ID of 1 (PID 1).</p>
<div class="language-bash ext-sh line-numbers-mode"><pre v-pre class="language-bash"><code>root          <span class="token number">1</span>      <span class="token number">0</span>  <span class="token number">0</span> 02:10 ?        00:00:02 /usr/lib/systemd/systemd --switched-root --system --deserialize <span class="token number">23</span>
</code></pre><div class="line-numbers" aria-hidden="true"><div class="line-number"></div></div></div><p>– systemd is the first process that starts after the system boots, and is the final process that is running when the system shuts down.
– It controls the final stages of booting and prepares the system for use. It also speeds up booting by loading services concurrently.
– systemd reads the file linked by /etc/systemd/system/default.target (for example, /usr/lib/systemd/system/multi-user.target) to determine the default system target (equivalent to run level). The system target file defines the services that systemd starts.
– systemd allows you to manage various types of units on a system, including services (name.service) and targets (name.target), devices (name.device), file system mount points (name.mount), and sockets (name.socket).</p>
