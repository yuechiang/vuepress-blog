<h2 id="_1-post-power-on-self-test" tabindex="-1"><a class="header-anchor" href="#_1-post-power-on-self-test" aria-hidden="true">#</a> 1. POST (Power on Self Test)</h2>
<p>From the system firmware, which can be the modern Universal Extended Firmware Interface (UEFI) or the classical Basic Input Output System (BIOS), the Power-On Self-Test (POST) is executed, and the hardware that is required to start the system is initialized.</p>
<h2 id="_2-selecting-the-bootable-device-with-mbr" tabindex="-1"><a class="header-anchor" href="#_2-selecting-the-bootable-device-with-mbr" aria-hidden="true">#</a> 2. Selecting the bootable device (With MBR)</h2>
<p>– Master Boot Record (MBR) is the first 512 bytes of the boot drive that is read into memory by the BIOS.
– The next 64 bytes contain the partition table for the disk. The last two bytes are the “Magic Number” which is used for error detection.</p>
<p><img src="/img/MBR.png" alt="mbr"></p>
<p>– MBR discovers the bootable device and loads the GRUB2 boot loader into memory and transfers control over to it.</p>
<h2 id="_3-loading-the-boot-loader-grub2" tabindex="-1"><a class="header-anchor" href="#_3-loading-the-boot-loader-grub2" aria-hidden="true">#</a> 3. Loading the boot loader (GRUB2)</h2>
<p>– The default bootloader program used on RHEL 7 is GRUB 2. GRUB stands for GRand Unified Bootloader. GRUB 2 replaces the older GRUB bootloader also called as legacy GRUB.
– The GRUB 2 configuration file is located at /boot/grub2/grub.cfg (Do not edit this file directly).
– GRUB 2 menu-configuration settings are taken from /etc/default/grub when generating grub.cfg.
– Sample /etc/default/grub file :</p>
