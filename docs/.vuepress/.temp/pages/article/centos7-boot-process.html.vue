<h2 id="introduction-of-systemd" tabindex="-1"><a class="header-anchor" href="#introduction-of-systemd" aria-hidden="true">#</a> Introduction of systemd</h2>
<p>systemd is the new system and service manager in CentOS/RHEL 7. It is backward compatible with SysV init scripts used by previous versions of RedHat Linux including RHEL 6. It replaces Upstart as the default initialization system.</p>
<p>The following steps summarize how the boot procedure happens in RHEL/CentOS 7.</p>
<ol>
<li>The computer’s BIOS performs POST.</li>
<li>BIOS reads the MBR for the bootloader.</li>
<li>GRUB 2 bootloader loads the vmlinuz kernel image.</li>
<li>GRUB 2 extracts the contents of the initramfs image.</li>
<li>The kernel loads driver modules from initramfs.</li>
<li>Kernel starts the system’s first process, systemd.</li>
<li>The systemd process takes over. It:</li>
</ol>
<ul>
<li>Reads configuration files from the /etc/systemd directory</li>
<li>Reads file linked by /etc/systemd/system/default.target</li>
<li>Brings the system to the state defined by the system target</li>
<li>Executes /etc/rc.local</li>
</ul>
